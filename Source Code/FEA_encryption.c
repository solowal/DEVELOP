void TS(unsigned char* TWEAK, unsigned char ROUND_TWEAK[][8]) {
	unsigned char TL[8] = { 0, };
	unsigned char TR[8] = { 0, };
	unsigned char na, nb = 0;
	na = ((64 - n2) + (8 - (64 - n2) % 8) % 8) >> 3;
	nb = ((64 - n1) + (8 - (64 - n1) % 8) % 8) >> 3;
	if (ENC_TYPE == 1) {

		TL[0] = TWEAK[0] >> (8 - (64 - n2) % 8);
		for (int i = 1; i < na; i++) {
			TL[i] = TWEAK[i - 1] << ((64 - n2) % 8) | TWEAK[i] >> (8 - ((64 - n2) % 8));
		}
		for (int i = 0; i < nb; i++) {

			TR[i] = TWEAK[i + na - 1] << ((128 - n) % 8) | TWEAK[i + na] >> (8 - ((128 - n) % 8));
		}

		for (int i = 0; i < ROUND; i += 2) {
			for (int j = 8 - na; j < 8; j++) {

				ROUND_TWEAK[i][j] = TL[j - (8 - na)];
			}
			for (int j = 8 - nb; j < 8; j++) {
				ROUND_TWEAK[i + 1][j] = TR[j - (8 - nb)];
			}

		}
	}
	else {
		for (int i = 0; i < ROUND; i += 3) {
			for (int j = 0; j < 8; j++) {
				ROUND_TWEAK[i + 1][j] = TWEAK[j];
				ROUND_TWEAK[i + 2][j] = TWEAK[j + 4];
			}
		}
	}


}

